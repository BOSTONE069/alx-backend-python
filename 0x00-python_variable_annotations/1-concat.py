#!/usr/bin/env python3

def concat(str1: str, str2: str) -> str:
    """
    `concat` takes two strings and returns their concatenation

    :param str1: str - This is the first parameter. It's a string
    :type str1: str
    :param str2: str - This is the second parameter, and it's a string
    :type str2: str
    :return: The concatenation of the two strings.
    """
    return str1 + str2
